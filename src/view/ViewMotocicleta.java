    private void limparCampos() {
        jtfId.setText("");
        jtfModelo.setText("");
        jtfValor.setText("");
        jtfEstoque.setText("");
    }
    
    private void salvarMotocicleta() {
        modelMotocicletas.setMotoModelo(this.jtfModelo.getText());
        modelMotocicletas.setMotoValor(formatador.converterVirgulaParaPonto(this.jtfValor.getText()));
        modelMotocicletas.setMotoEstoque(Integer.parseInt(this.jtfEstoque.getText()));
        
        if (controllerMotocicletas.salvarMotocicletaController(modelMotocicletas)>0) {
            JOptionPane.showMessageDialog(this, "Moto cadastrada!");
            this.carregarMotocicletas();
            limparCampos();
            
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar motocicleta", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void alterarMotocicleta() {
        modelMotocicletas.setMotoModelo(this.jtfModelo.getText());
        modelMotocicletas.setMotoValor(Double.parseDouble(this.jtfValor.getText()));
        modelMotocicletas.setMotoEstoque(Integer.parseInt(this.jtfEstoque.getText()));
        
        if (controllerMotocicletas.alterarMotocicletaController(modelMotocicletas)) {
            JOptionPane.showMessageDialog(this, "Moto alterada!");
            this.carregarMotocicletas();
            limparCampos();
            
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar motocicleta", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
        salvarAlterar = "salvar";
    }
    
    private void carregarMotocicletas() {
        listaModelMotocicletas = controllerMotocicletas.retornarListaMotocicletaController();
        
        DefaultTableModel modelo = (DefaultTableModel) jtableMotocicletas.getModel();
        modelo.setNumRows(0);
        
        int cont = listaModelMotocicletas.size();
        for (int i =0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelMotocicletas.get(i).getIdMotocicleta(),
                listaModelMotocicletas.get(i).getMotoModelo(),
                listaModelMotocicletas.get(i).getMotoValor(),
                listaModelMotocicletas.get(i).getMotoEstoque()
            });
        }
    }

