    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        int linha = jtableMotocicletas.getSelectedRow();
        
        try {
            int codigoMotocicleta = (int) this.jtableMotocicletas.getValueAt(linha, 0);
            modelMotocicletas = controllerMotocicletas.retornarMotocicletaController(codigoMotocicleta);
            
            jtfId.setText(String.valueOf(modelMotocicletas.getIdMotocicleta()));
            jtfModelo.setText(modelMotocicletas.getMotoModelo());
            jtfValor.setText(String.valueOf(modelMotocicletas.getMotoValor()));
            jtfEstoque.setText(String.valueOf(modelMotocicletas.getMotoEstoque()));
            
            salvarAlterar = "alterar";
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nenhum registro selecionado", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        if (salvarAlterar.equals("salvar")) {
            this.salvarMotocicleta();
        } else {
            this.alterarMotocicleta();
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        int linha = jtableMotocicletas.getSelectedRow();
        int codigoMotocicleta = (int) jtableMotocicletas.getValueAt(linha, 0);
        
        if(controllerMotocicletas.excluirMotocicletaController(codigoMotocicleta)) {
            JOptionPane.showMessageDialog(this, "Moto excluida!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.carregarMotocicletas();
            
        }else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

